def calculate_reflection_number(pattern):
    rows, cols = len(pattern), len(pattern[0])

    # Check for a vertical reflection
    for mirror in range(cols - 1):
        if all(
            pattern[row][col] == pattern[row][mirror + mirror - col + 1]
            for row in range(rows)
            for col in range(mirror - min(mirror, cols - mirror - 2), mirror + 1)
        ):
            return mirror + 1

    # Check for a horizontal reflection
    for mirror in range(rows - 1):
        if all(
            pattern[row][col] == pattern[mirror + mirror - row + 1][col]
            for col in range(cols)
            for row in range(mirror - min(mirror, rows - mirror - 2), mirror + 1)
        ):
            return (mirror + 1) * 100

    return 0  # No reflection found


